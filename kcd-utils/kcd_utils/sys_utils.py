import subprocess

# ===================================================================================================
# System Utility/Helper Methods
# ===================================================================================================
def open_explorer(path):
    '''
    Opens Windows Explorer at the specified path

    :param path: The target path
    :type path: str
    '''
    cmd = 'explorer "%s"' % path
    subprocess.Popen(cmd)
