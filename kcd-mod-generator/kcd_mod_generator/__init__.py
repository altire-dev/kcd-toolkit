# ===================================================================================================
# Imports: External
# ===================================================================================================
import wx

# ===================================================================================================
# Imports: Internal
# ===================================================================================================
from .gui import KCDModGeneratorGui

# ===================================================================================================
# Properties
# ===================================================================================================
VERSION = "1.1.1"
AUTHOR = "Altire"


# ===================================================================================================
# KCD Mod Generator Class
# ===================================================================================================
class KCDModGenerator:
    '''
    KCD Mod Generator. Main object for KCD Mod Generator Package
    '''

    # ===================================================================================================
    # Methods
    # ===================================================================================================
    def __init__(self):
        '''
        Constructor
        '''
        # Initialise the GUI
        self._app = wx.App()
        self._gui = KCDModGeneratorGui(VERSION, AUTHOR)

    def launch(self):
        '''
        Launches the app
        '''
        self._gui.Show()
        self._app.MainLoop()